public class NumException {  // NumException 클래스
	public static void main(String[] args) {  // main() 함수
		String[] stringNumber = {"23", "12", "3.141592", "998"};  // String형 배열 선언 + 초기화
		
		int i = 0;  // 반복문에서 사용되는 변수 i
		try  // 예외가 발생할 가능성이 있는 실행문
		{
			for(i=0; i<stringNumber.length; i++)  // i는 0부터 stringNumber의 전체 문자 길이까지 반복
			{
				int j = Integer.parseInt(stringNumber[i]);  // 문자열을 정수형으로 변환
				System.out.println("숫자로 변환된 값은 " + j);  // 정수가 입력되었을 때
			}
		}
		catch (NumberFormatException e)  // 처리할 예외 타입: 문자열이 나타내는 숫자와 일치하지 않는 타입의 숫자로 변환 시 발생
		{
			System.out.println(stringNumber[i] + "는 정수로 변환할 수 없습니다.");  // 정수가 아닌 다른 것이 입력되었을 때
		}
	}  // finally 블록 생략
}